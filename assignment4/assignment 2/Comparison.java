class Comparison 
{
public static void main( String args[] )    
{
        int num1=50;      
        int num2=75;               
      
        if ( num1 == num2 )           
            System.out.println(num1+"==" +num2 );  
		
        if ( num1 != num2 )          
            System.out.println(num1+"!=" +num2 ); 
		
        if ( num1 < num2 )          
            System.out.println (num1+"<" +num2 ); 
		
        if ( num1 > num2 )          
            System.out.println(num1+">" +num2);  
		
        if ( num1 <= num2 )          
            System.out.println(num1+"<=" +num2 );  
		
        if ( num1 >= num2 )          
            System.out.println(num1+">=" +num2 );  
		
}
}